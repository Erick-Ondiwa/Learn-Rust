fn main (){
   //defining and assigning values to variables
  let length = 4;
  let width = 5;

  let area = length * width; //calculating the area of the rectangle

  println!("The area of the rectangle is: {}", area); //displayng the output

  //Use of constants
  const PI: f32 = 3.142;
  let radius = 14.0;

  let circle_area = PI * (radius * radius);

  println!("The area of the circle is {}", circle_area);
}